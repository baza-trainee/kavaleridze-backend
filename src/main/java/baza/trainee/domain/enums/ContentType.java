package baza.trainee.domain.enums;

/**
 * An enumeration representing different content types.
 * This enum defines two content types: ARTICLE and EVENT.
 * Content types can be used to categorize different types of data or content.
 */
public enum ContentType {
    /**
     * Represents the content type as an ARTICLE.
     * This type is typically used for textual articles or written content.
     */
    ARTICLE,

    /**
     * Represents the content type as an EVENT.
     * This type is typically used for events, such as meetings.
     */
    EVENT
}
